def test_exporter_official(lts_db):
    """Test PipelineExporter using data generated by tracker."""
    tracker = PipelineTracker(lts_db)
    reg = tracker.register_pipeline("export_target", [])
    tracker.complete_pipeline(reg["pipeline_id"])
    
    exporter = PipelineExporter(lts_db)
    
    # Export JSON
    json_data = exporter.export_pipeline_logs(format="json")
    data = json.loads(json_data)
    assert any(d["name"] == "export_target" for d in data)
    
    # Export Stats
    stats_json = exporter.export_statistics()
    stats = json.loads(stats_json)
    assert stats["total_executions"] >= 1
